let lang = "en";

function applyLang() {
  const t = T[lang];
  document.querySelectorAll("[data-t]").forEach(el => {
    const k = el.getAttribute("data-t");
    if (t[k]) el.textContent = t[k];
  });
  document.querySelectorAll("[data-ph]").forEach(el => {
    const k = el.getAttribute("data-ph");
    if (t[k]) el.placeholder = t[k];
  });
  document.getElementById("btnLang").textContent = lang === "en" ? "🇫🇷 FR" : "🇬🇧 EN";
}

function toggleLang() {
  lang = lang === "en" ? "fr" : "en";
  applyLang();
}

// scroll nav highlight + fade
function onScroll() {
  const y = window.scrollY + 80;
  document.querySelectorAll("section[id]").forEach(sec => {
    const a = document.querySelector(`.nav-links a[href="#${sec.id}"]`);
    if (!a) return;
    if (y >= sec.offsetTop && y < sec.offsetTop + sec.offsetHeight) {
      document.querySelectorAll(".nav-links a").forEach(l => l.classList.remove("active"));
      a.classList.add("active");
    }
  });
  document.querySelectorAll(".fade").forEach(el => {
    if (el.getBoundingClientRect().top < window.innerHeight - 60) el.classList.add("visible");
  });
}

document.addEventListener("DOMContentLoaded", () => {
  applyLang();
  onScroll();
  window.addEventListener("scroll", onScroll);
});

